def gen_message_registry_h(messages):
    lines = []

    lines.append("#ifndef FIXTURE_GENERATED_MESSAGE_REGISTRY_H")
    lines.append("#define FIXTURE_GENERATED_MESSAGE_REGISTRY_H")
    lines.append("")
    lines.append("// ============================================================")
    lines.append("// message_registry.h  --  AUTO-GENERATED FILE, DO NOT EDIT MANUALLY")
    lines.append("// Generated by generate_messages.py from messages.def")
    lines.append("// ============================================================")
    lines.append("")
    lines.append('#include "messages.h"')
    lines.append("")
    lines.append("#include <map>")
    lines.append("#include <typeindex>")
    lines.append("#include <functional>")
    lines.append("#include <vector>")
    lines.append("#include <string>")
    lines.append("")
    lines.append("// ============================================================")
    lines.append("// MessageRegistry")
    lines.append("//")
    lines.append("// A singleton registry that maps message type names to factory")
    lines.append("// functions.  Each factory produces a default-serialised")
    lines.append("// instance of the corresponding message type.")
    lines.append("// ============================================================")
    lines.append("")
    lines.append("class MessageRegistry")
    lines.append("{")
    lines.append("public:")
    lines.append("")
    lines.append("    // Factory function type: produces a serialised default instance.")
    lines.append("    using FactoryFn = std::function<std::string()>;")
    lines.append("")
    lines.append("    // ----------------------------------------------------------")
    lines.append("    // Singleton access")
    lines.append("    // ----------------------------------------------------------")
    lines.append("    static MessageRegistry& instance();")
    lines.append("")
    lines.append("    // ----------------------------------------------------------")
    lines.append("    // Registration")
    lines.append("    // ----------------------------------------------------------")
    lines.append("    void register_type(const std::string& name, FactoryFn factory);")
    lines.append("")
    lines.append("    // ----------------------------------------------------------")
    lines.append("    // Queries")
    lines.append("    // ----------------------------------------------------------")
    lines.append("    bool has_type(const std::string& name) const;")
    lines.append("")
    lines.append("    std::vector<std::string> registered_types() const;")
    lines.append("")
    lines.append("private:")
    lines.append("")
    lines.append("    // Private constructor — use instance() instead.")
    lines.append("    MessageRegistry() = default;")
    lines.append("")
    lines.append("    // The map from type name -> factory function.")
    lines.append("    std::map<std::string, FactoryFn> factories_;")
    lines.append("")
    lines.append("};  // class MessageRegistry")
    lines.append("")
    lines.append("#endif // FIXTURE_GENERATED_MESSAGE_REGISTRY_H")
    lines.append("")

    return "\n".join(lines)


