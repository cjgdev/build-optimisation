def gen_message_registry_cpp(messages):
    lines = []

    lines.append("// ============================================================")
    lines.append("// message_registry.cpp  --  AUTO-GENERATED FILE, DO NOT EDIT MANUALLY")
    lines.append("// Generated by generate_messages.py from messages.def")
    lines.append("// ============================================================")
    lines.append("")
    lines.append('#include "message_registry.h"')
    lines.append("")

    # ----- instance() -----
    lines.append("// ------------------------------------------------------------------")
    lines.append("// MessageRegistry::instance()")
    lines.append("// Returns the process-wide singleton.")
    lines.append("// ------------------------------------------------------------------")
    lines.append("MessageRegistry& MessageRegistry::instance()")
    lines.append("{")
    lines.append("    static MessageRegistry registry;")
    lines.append("    return registry;")
    lines.append("}")
    lines.append("")

    # ----- register_type() -----
    lines.append("// ------------------------------------------------------------------")
    lines.append("// MessageRegistry::register_type()")
    lines.append("// Associates a type name with a factory function.")
    lines.append("// ------------------------------------------------------------------")
    lines.append("void MessageRegistry::register_type(const std::string& name, FactoryFn factory)")
    lines.append("{")
    lines.append("    factories_[name] = factory;")
    lines.append("}")
    lines.append("")

    # ----- has_type() -----
    lines.append("// ------------------------------------------------------------------")
    lines.append("// MessageRegistry::has_type()")
    lines.append("// Returns true if the given type name has been registered.")
    lines.append("// ------------------------------------------------------------------")
    lines.append("bool MessageRegistry::has_type(const std::string& name) const")
    lines.append("{")
    lines.append("    return factories_.find(name) != factories_.end();")
    lines.append("}")
    lines.append("")

    # ----- registered_types() -----
    lines.append("// ------------------------------------------------------------------")
    lines.append("// MessageRegistry::registered_types()")
    lines.append("// Returns the names of all registered types in sorted order.")
    lines.append("// ------------------------------------------------------------------")
    lines.append("std::vector<std::string> MessageRegistry::registered_types() const")
    lines.append("{")
    lines.append("    std::vector<std::string> names;")
    lines.append("    names.reserve(factories_.size());")
    lines.append("    for (const auto& kv : factories_)")
    lines.append("    {")
    lines.append("        names.push_back(kv.first);")
    lines.append("    }")
    lines.append("    return names;")
    lines.append("}")
    lines.append("")

    # ----- RegistryInitializer -----
    lines.append("// ------------------------------------------------------------------")
    lines.append("// RegistryInitializer")
    lines.append("//")
    lines.append("// A namespace-scope struct whose constructor registers all known")
    lines.append("// message types.  The static instance below ensures this runs")
    lines.append("// before main().")
    lines.append("// ------------------------------------------------------------------")
    lines.append("namespace")
    lines.append("{")
    lines.append("")
    lines.append("struct RegistryInitializer")
    lines.append("{")
    lines.append("    RegistryInitializer()")
    lines.append("    {")
    lines.append("        MessageRegistry& reg = MessageRegistry::instance();")
    lines.append("")
    for msg in messages:
        n = msg["name"]
        lines.append("        // Register " + n)
        lines.append("        reg.register_type(")
        lines.append('            "' + n + '",')
        lines.append("            []() -> std::string")
        lines.append("            {")
        lines.append("                return " + n + "().serialize();")
        lines.append("            });")
        lines.append("")
    lines.append("    }  // RegistryInitializer()")
    lines.append("};  // struct RegistryInitializer")
    lines.append("")
    lines.append("// Trigger registration at program startup.")
    lines.append("static RegistryInitializer init;")
    lines.append("")
    lines.append("}  // anonymous namespace")
    lines.append("")

    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Entry point
# ---------------------------------------------------------------------------


def main():
    if len(sys.argv) != 3:
        print("Usage: python3 generate_messages.py <def_file> <output_dir>", file=sys.stderr)
        sys.exit(1)

    def_file = sys.argv[1]
    output_dir = sys.argv[2]

    if not os.path.isfile(def_file):
        print("Error: def file not found: " + def_file, file=sys.stderr)
        sys.exit(1)

    os.makedirs(output_dir, exist_ok=True)

    print("Parsing: " + def_file)
    messages = parse_def_file(def_file)
    print("Found " + str(len(messages)) + " message(s): " + ", ".join(m["name"] for m in messages))

    files = {
        "messages.h": gen_messages_h(messages),
        "messages.cpp": gen_messages_cpp(messages),
        "message_registry.h": gen_message_registry_h(messages),
        "message_registry.cpp": gen_message_registry_cpp(messages),
    }

    for filename, content in files.items():
        out_path = os.path.join(output_dir, filename)
        with open(out_path, "w") as fh:
            fh.write(content)
        line_count = content.count("\n")
        print("Wrote: " + out_path + "  (" + str(line_count) + " lines)")

    print("Done.")


if __name__ == "__main__":
    main()
